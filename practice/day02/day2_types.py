# a) 字符串 "99" → 整数，加 1 → 打印
num_str = "99"
num_int = int(num_str)          # 字符串转整数
result_a = num_int + 1
print(f"a) '99' + 1 = {result_a}")

# b) 整数 8 → 小数，除以 3 → 打印
num_int2 = 8
num_float = float(num_int2)    # 整数转小数
result_b = num_float / 3
print(f"b) 8 / 3 = {result_b:.4f}")

# c) 3.14 → 整数（截断小数部分）→ 打印
pi = 3.14
result_c = int(pi)             # 小数转整数，小数部分丢失
print(f"c) int(3.14) = {result_c}")

# ══════════════════════════════════════════════════════════════
# 进阶：把答案和你的理解打印出来
# ══════════════════════════════════════════════════════════════
print("\n我的理解：")
print(f"  int('99') = {int('99')}，字符串变整数")
print(f"  float(8) / 3 = {float(8)/3:.4f}，整数变小数后可以除")
print(f"  int(3.14) = {int(3.14)}，小数转整数会截断（不是四舍五入）")
